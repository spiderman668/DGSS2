def standardize_smiles(smiles):
    mol = Chem.MolFromSmiles(smiles)
    return Chem.MolToSmiles(mol)


def smile2graph(smile):
    smile = standardize_smiles(smile)
    mol = Chem.MolFromSmiles(smile)
    max_atoms = 100
    # atoms
    atom_features_list = []
    for atom in mol.GetAtoms():
        atom_features_list.append(atom_to_feature_vector(atom))
    x = np.array(atom_features_list, dtype=np.int64)

    # bonds
    num_bond_features = 3  # bond type, bond stereo, is_conjugated
    if len(mol.GetBonds()) > 0:  # mol has bonds
        edges_list = []
        edge_features_list = []
        for bond in mol.GetBonds():
            i = bond.GetBeginAtomIdx()
            j = bond.GetEndAtomIdx()
            edge_feature = bond_to_feature_vector(bond)

            # add edges in both directions
            edges_list.append((i, j))
            edge_features_list.append(edge_feature)
            edges_list.append((j, i))
            edge_features_list.append(edge_feature)

        # data.edge_index: Graph connectivity in COO format with shape [2, num_edges]
        edge_index = np.array(edges_list, dtype=np.int64).T
        # data.edge_attr: Edge feature matrix with shape [num_edges, num_edge_features]
        edge_attr = np.array(edge_features_list, dtype=np.int64)

    else:  # mol has no bonds
        edge_index = np.empty((2, 0), dtype=np.int64)
        edge_attr = np.empty((0, num_bond_features), dtype=np.int64)

    x = torch.tensor(x, dtype=torch.long)
    x = mol_to_single_emb(x)
    # padding
    num_atoms = x.shape[0]
    if num_atoms < max_atoms:
        pad_size = max_atoms - num_atoms
        x = torch.cat([x, torch.zeros((pad_size, x.shape[1]), dtype=x.dtype)], dim=0)
        mask = torch.cat([torch.zeros(num_atoms, dtype=torch.bool), torch.ones(pad_size, dtype=torch.bool)], dim=0)
    else:
        x = x[:max_atoms]
        mask = torch.zeros(max_atoms, dtype=torch.bool)
    valid_edges_mask = (edge_index[0] < max_atoms) & (edge_index[1] < max_atoms)
    edge_index = edge_index[:, valid_edges_mask]
    edge_attr = edge_attr[valid_edges_mask]
    edge_attr = torch.tensor(edge_attr, dtype=torch.long)
    edge_attr = mol_to_single_emb(edge_attr)
    
